// ---- On Load: Set Theme, Language, Menu ----
window.addEventListener('DOMContentLoaded', () => {
  // Saved Language
  const savedLang = localStorage.getItem('selectedLanguage');
  if (savedLang) {
    languageSelect.value = savedLang;
    loadLanguage(savedLang);
  } else {
    showHomePage();
  }

  // Saved Theme
  const savedTheme = localStorage.getItem('theme');
  if (savedTheme === 'dark') {
    document.body.classList.add('dark-mode');
    modeToggle.textContent = 'ЁЯМЩ';
  } else {
    document.body.classList.remove('dark-mode');
    modeToggle.textContent = 'тШАя╕П';
  }

  setupMenuToggle();
  setupModeToggle();
});

// ---- Menu Toggle ----
function setupMenuToggle() {
  const menuToggle = document.getElementById('menu-toggle');
  const sideMenu = document.getElementById('side-menu');
  const closeMenu = document.getElementById('close-menu');
  menuToggle.addEventListener('click', () => sideMenu.classList.add('active'));
  closeMenu.addEventListener('click', () => sideMenu.classList.remove('active'));
  document.addEventListener('click', (e) => {
    if (!sideMenu.contains(e.target) && !menuToggle.contains(e.target)) {
      sideMenu.classList.remove('active');
    }
  });
}

// ---- Dark/Light Mode Toggle ----
function setupModeToggle() {
  modeToggle.addEventListener('click', () => {
    document.body.classList.toggle('dark-mode');
    const isDark = document.body.classList.contains('dark-mode');
    modeToggle.textContent = isDark ? 'ЁЯМЩ' : 'тШАя╕П';
    localStorage.setItem('theme', isDark ? 'dark' : 'light');
  });
}

// ---- Language Selection ----
languageSelect.addEventListener('change', () => {
  const lang = languageSelect.value;
  if (!lang) {
    showHomePage();
    return;
  }
  localStorage.setItem('selectedLanguage', lang);
  loadLanguage(lang);
});

// ---- Load Language Data ----
function loadLanguage(lang) {
  currentLanguage = lang;
  showLoadingState();
  fetch(`languages/${lang}.json`)
    .then(res => {
      if (!res.ok) throw new Error(`HTTP ${res.status}: ${res.statusText}`);
      return res.json();
    })
    .then(data => {
      if (!Array.isArray(data) || data.length === 0) throw new Error('Data format is invalid or empty');
      currentData = data;
      hideHomePage();
      showFolderControls();
      renderVocabulary(data, langCodeMap[lang]);
    })
    .catch(error => {
      showError(`ржнрж╛рж╖рж╛рж░ ржбрзЗржЯрж╛ рж▓рзЛржб ржХрж░рждрзЗ рж╕ржорж╕рзНржпрж╛ рж╣ржпрж╝рзЗржЫрзЗ: ${error.message}`);
    });
}

